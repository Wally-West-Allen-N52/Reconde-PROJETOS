package Menu;

public class Transporte {
	
	private String aviao;
	private String carro;
	
	public void setAviao(String aviao) {
		this.aviao = aviao;
	}
	public String getAviao() {
		return this.aviao;
	}
	public void setCarro(String carro) {
		this.carro = carro;
	}
	public String getCarro() {
		return this.carro;
	}
}
